    private void jGuardarLogActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jGuardarLogActionPerformed
       traspasos.mkdir();
        try {
            FileWriter log = new FileWriter (traspasos + "\\logTraspasos.txt", true);
            BufferedWriter bW = new BufferedWriter (log);
            PrintWriter pW = new PrintWriter (bW);
            StringBuilder s = new StringBuilder(jLogTraspasos.getText());
            s.delete(0, s.indexOf("\n"));
            s = new StringBuilder (s.substring(s.indexOf("E")));
            while (s.toString().contains(".")){
                pW.println(s.substring(0, s.indexOf(".") + 1));
                s.delete(0, s.indexOf(".") + 1);
            }
            pW.println();
            pW.close();
        } catch (IOException ex) {
            System.out.println("Error");
        }
    }//GEN-LAST:event_jGuardarLogActionPerformed

    private void jCargarLogActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCargarLogActionPerformed
       File log = new File (traspasos + "\\logTraspasos.txt");
       if (log.exists()){
           try {
               FileReader lectura = new FileReader (log);
               BufferedReader bR = new BufferedReader (lectura);
               String s, escribir = "";
               while ((s = bR.readLine())!= null){
                   jLogTraspasos.setText(jLogTraspasos.getText() + s + "\n");
               }
               
           } catch (FileNotFoundException ex) {
               System.out.println ("Fichero no encontrado");
           } catch (IOException ex) {
               System.out.println("Error");
           }
       }
    }//GEN-LAST:event_jCargarLogActionPerformed


    private void jTraspasarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTraspasarActionPerformed
        if ((jComboBoxEqOrigen.getSelectedItem() != "Origen") && (jComboBoxEqDestino.getSelectedItem() != "Destino")){
            ArrayList <JComboBox> contenedorOrigen = new ArrayList();
            ArrayList <JComboBox> contenedorDestino = new ArrayList();
            contenedorOrigen.add(jComboBoxJ1);
            contenedorOrigen.add(jComboBoxJ2);
            contenedorOrigen.add(jComboBoxJ3);
            contenedorOrigen.add(jComboBoxJ4);
            contenedorDestino.add(jComboBoxJ5);
            contenedorDestino.add(jComboBoxJ6);
            contenedorDestino.add(jComboBoxJ7);
            contenedorDestino.add(jComboBoxJ8);
            for (JComboBox selector: contenedorOrigen){
                if (selector.isVisible() && selector.getSelectedItem() != ""){
                    jLogTraspasos.setText(jLogTraspasos.getText() + "\n" + liga.traspasarJugador(jComboBoxEqOrigen.getSelectedItem().toString(), jComboBoxEqDestino.getSelectedItem().toString(), liga.buscarJugador(selector.getSelectedItem().toString())));
                }
            }
            for (JComboBox selector: contenedorDestino){
                if (selector.isVisible() && selector.getSelectedItem() != ""){
                    jLogTraspasos.setText(jLogTraspasos.getText() + "\n" + liga.traspasarJugador(jComboBoxEqDestino.getSelectedItem().toString(), jComboBoxEqOrigen.getSelectedItem().toString(), liga.buscarJugador(selector.getSelectedItem().toString())));
                }
            }
            
        }else{jLogTraspasos.setText("Error en el traspaso, no se especifico equipo de origen y/o equipo de destino");}
        
    }//GEN-LAST:event_jTraspasarActionPerformed


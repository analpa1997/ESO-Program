    private void jComboBoxEqOrigenItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBoxEqOrigenItemStateChanged
        if (jComboBoxEqOrigen.getSelectedItem() != "Origen"){
            jComboBoxEqDestino.setVisible(true);
            jComboBoxJ1.setVisible(true);
            jComboBoxEqDestino.removeItem(jComboBoxEqOrigen.getSelectedItem());
            jComboBoxJ1.removeAllItems();
            jComboBoxJ1.addItem("");
            jComboBoxJ2.removeAllItems();
            jComboBoxJ2.addItem("");
            jComboBoxJ3.removeAllItems();
            jComboBoxJ3.addItem("");
            jComboBoxJ4.removeAllItems();
            jComboBoxJ4.addItem("");
            Iterator it = liga.getEquipos().iterator();
            Equipo e = liga.buscarEquipo(jComboBoxEqOrigen.getSelectedItem().toString());
            for (Jugador j: e.getRoster()){
                jComboBoxJ1.addItem(j.getNombre());
                jComboBoxJ2.addItem(j.getNombre());
                jComboBoxJ3.addItem(j.getNombre());
                jComboBoxJ4.addItem(j.getNombre());
            }
         }
        
    }//GEN-LAST:event_jComboBoxEqOrigenItemStateChanged

